/*データ型について */

class DataType{
    public static void main(String[] args){
        int age = 20;
        float weight = 63.0f;
        double height = 168.5;
        char bloodType = 'A';
        String name = "Marry";
        System.out.println("Hello, I am " + name);
        System.out.println("I am " + age + " years old");
        System.out.println("I am " + height + "cm tall, and weight of " + weight + "kg. My bloodtype is " + bloodType);
    }
}